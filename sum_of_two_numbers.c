#include <stdio.h>

/**
 * main - The entry point of the program
 *
 * Description: This function calculates the sum of two integers, num1 and num2,
 * and prints the result to the console.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
    int num1 = 10, num2 = 20, sum;

    sum = num1 + num2;
    printf("sum: %d\n", sum);

    return 0;
}
